  CREATE OR REPLACE FUNCTION "REGISTRAR_VENTA" (
    p_id_usuario IN NUMBER,
    p_codigo_videojuego IN NUMBER,
    p_cantidad IN NUMBER,
    p_id_venta IN NUMBER
) RETURN NUMBER IS
    v_id_venta NUMBER;
    v_nivel nivel.nivelusuario%TYPE;
    v_valor_videojuego videojuego.precio%TYPE;
BEGIN
