  CREATE OR REPLACE FUNCTION "CALCULAR_NIVEL_USUARIO" (
    p_id_usuario IN NUMBER
) RETURN NUMBER IS
    v_nivel_usuario NUMBER;
    v_cantidad_afiliados NUMBER;
    v_cantidad_ventas NUMBER;
BEGIN
    -- Obtener la cantidad de afiliados del usuario
    SELECT COUNT(*) INTO v_cantidad_afiliados FROM usuario u
    WHERE u.codigoafliacion = p_id_usuario;

    -- Obtener la cantidad total de ventas del usuario
    SELECT n.cantidadventas INTO v_cantidad_ventas
    FROM nivel n
    WHERE n.usuario_cedula = p_id_usuario;

    -- Calcular el nivel de usuario seg?n las condiciones
    IF v_cantidad_afiliados > 2 AND v_cantidad_ventas > 350 THEN
        v_nivel_usuario := 2;

    ELSE IF v_cantidad_afiliados > 9 AND v_cantidad_ventas > 399 THEN
        v_nivel_usuario := 3;

    ELSE IF v_cantidad_afiliados > 20 AND v_cantidad_ventas > 450 THEN
        v_nivel_usuario := 4;

    ELSE IF v_cantidad_afiliados > 30 AND v_cantidad_ventas > 550 THEN
        v_nivel_usuario := 5;

    ELSE
        v_nivel_usuario := 1;
    END IF;
    END IF;
    END IF;
    END IF;

    -- Retornar el nivel de usuario calculado
    RETURN v_nivel_usuario;
END;

