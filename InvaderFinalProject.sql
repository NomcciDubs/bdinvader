    VALUES (p_id_venta, p_codigo_videojuego, p_cantidad)
    RETURNING idventa INTO v_id_venta;

    --Obtener el precio del videojuego a vender
    SELECT v.precio INTO v_valor_videojuego FROM videojuego v
    WHERE v.codigo = p_codigo_videojuego;

    --Aumentar el saldo en la billetera
    UPDATE billetera bll
    SET bll.saldo = v_valor_videojuego * p_cantidad
    WHERE bll.usuario_cedula = p_id_usuario;

    -- Actualizar el nivel de usuario si corresponde
    v_nivel := calcular_nivel_usuario(p_id_usuario);
